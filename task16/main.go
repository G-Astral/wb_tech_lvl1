// Реализовать быструю сортировку массива (quicksort)
// встроенными методами языка.

package main

import (
	"fmt"
	"sort"
)

func main() {
	arr1 := []int{
		345, 728, 99, 532, 413, 872, 156, 274, 617, 901,
		231, 84, 767, 620, 413, 257, 945, 372, 81, 523,
		654, 782, 97, 320, 119, 468, 805, 390, 64, 923,
	}

	sort.Ints(arr1)

	fmt.Println(arr1)
}
